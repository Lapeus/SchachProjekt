package daten;

public class Spieler {

    private String name;
    private boolean farbe;
    private int spielzeit;
    private int anzahlZuege;
    
    public String getName() {
        return name;
    }
    
    public boolean getFarbe() {
        return farbe;
    }
    
    public int getSpielzeit() {
        return spielzeit;
    }
    
    public int getAnzahlZuege() {
        return anzahlZuege;
    }
}